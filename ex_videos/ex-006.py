t = int(input('digite um numero inteiro ai: '))
print('o dobro de {} é: {}'.format(t, t*2))
print('o triplo de {} é: {}'.format(t, t*3))
# não é o numero elevado a potência 2, mas sim a raiz quadrada do numero
print('e sua raiz quadrada é: {:.2f}'.format(t**(1/2)))
print('a raiz cúbica é: {:.2f}'.format(t**(1/3)))
print('a metade é: {}'.format(t/2))
print('e {}, vezes o seu sucessor, que é {}, dá: {}'.format(t, t+1, t*(t+1)))
